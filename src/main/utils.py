def add(a, b):
    """Returns the sum of two numbers."""
    return a + b


def multiply(a, b):
    """Returns the product of two numbers."""
    return a * b


def greet(name):
    """Returns a greeting message."""
    return f"Hello, {name}!"


if __name__ == "__main__":
    print(add(2, 3))
    print(multiply(4, 5))
    print(greet("Alice"))
